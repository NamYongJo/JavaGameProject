package characters.monster;

public class Skeleton extends Monster {

	public Skeleton(String name, int hp, int mp, int level, int power, int def, int money, int exp) {
		this.name = name;
		this.hp = hp;
		this.mp = mp;
		this.level = level;
		this.power = power;
		this.def = def;
		this.money = money;
		this.exp = exp;
	}

}
